// Switchcase
let fruit = "mango"
switch(fruit){
    case "apple": console.log("apple : Rs. 40");
    break;
    case "banana": console.log("banana : Rs. 30");
    break;
    case "mango": console.log("mango : Rs. 50");
    break;
    default: console.log("Invalid input");
}

